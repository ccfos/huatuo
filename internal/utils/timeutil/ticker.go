package timeutil

import (
	"context"
	"time"
)

// RunEvery calls fn on every tick of d until ctx is done.
func RunEvery(ctx context.Context, d time.Duration, fn func()) {
	fn()
	ticker := time.NewTicker(d)
	defer ticker.Stop()
	for {
		select {
		case <-ctx.Done():
			return
		case <-ticker.C:
			fn()
		}
	}
}
